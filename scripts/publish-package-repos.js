#!/usr/bin/env node
/**
 * Publish release artifacts to self-hosted APT/YUM repos (via SSH) and update the
 * Homebrew tap (git push). Skips any step when the required env vars are missing.
 *
 * Env (APT/YUM):
 *   PACKAGE_REPO_SSH          e.g. deploy@packages.myhomegames.vige.it
 *   PACKAGE_REPO_APT_ROOT     e.g. /var/www/packages/apt
 *   PACKAGE_REPO_YUM_ROOT     e.g. /var/www/packages/yum/el9/x86_64
 *   PACKAGE_REPO_APT_CODENAME default: stable
 *
 * Env (Homebrew tap):
 *   HOMEBREW_TAP_REPO         e.g. git@github.com:myhomegames/myhomegames-homebrewtap.git
 *   GITHUB_RELEASE_BASE       default: https://github.com/myhomegames/myhomegames-server/releases/download
 *
 * Loads `.env` and `.env.local` (if present) from the project root.
 */

const fs = require('fs');
const path = require('path');
const os = require('os');
const crypto = require('crypto');
const { execSync } = require('child_process');

const ROOT = path.join(__dirname, '..');

require('dotenv').config({ path: path.join(ROOT, '.env'), quiet: true });
require('dotenv').config({ path: path.join(ROOT, '.env.local'), override: true, quiet: true });

const BUILD_DIR = path.join(ROOT, 'build');
const packageJson = JSON.parse(fs.readFileSync(path.join(ROOT, 'package.json'), 'utf8'));
const version = packageJson.version;

const GITHUB_RELEASE_BASE =
  process.env.GITHUB_RELEASE_BASE ||
  'https://github.com/myhomegames/myhomegames-server/releases/download';

function log(msg) {
  console.log(`[publish] ${msg}`);
}

function warn(msg) {
  console.warn(`[publish] ⚠️  ${msg}`);
}

function sha256File(filePath) {
  const hash = crypto.createHash('sha256');
  hash.update(fs.readFileSync(filePath));
  return hash.digest('hex');
}

function run(cmd, opts = {}) {
  execSync(cmd, { stdio: 'inherit', ...opts });
}

function artifactPaths() {
  const deb = path.join(BUILD_DIR, `myhomegames-server_${version}_amd64.deb`);
  const rpm = path.join(BUILD_DIR, `myhomegames-server-${version}-1.x86_64.rpm`);
  const pkgArm64 = path.join(BUILD_DIR, `MyHomeGames-${version}-mac-arm64.pkg`);
  const pkgX64 = path.join(BUILD_DIR, `MyHomeGames-${version}-mac-x64.pkg`);
  return { deb, rpm, pkgArm64, pkgX64 };
}

function publishApt(debPath) {
  const ssh = process.env.PACKAGE_REPO_SSH;
  const aptRoot = process.env.PACKAGE_REPO_APT_ROOT;
  if (!ssh || !aptRoot) {
    warn('APT: skipped (set PACKAGE_REPO_SSH and PACKAGE_REPO_APT_ROOT to enable)');
    return;
  }
  if (!fs.existsSync(debPath)) {
    warn(`APT: skipped (.deb not found: ${debPath})`);
    return;
  }

  const codename = process.env.PACKAGE_REPO_APT_CODENAME || 'stable';
  const remoteDeb = `/tmp/myhomegames-server_${version}_amd64.deb`;

  log(`APT: uploading ${path.basename(debPath)} → ${ssh}`);
  run(`scp "${debPath}" "${ssh}:${remoteDeb}"`);
  run(
    `ssh "${ssh}" "reprepro -b ${shellQuote(aptRoot)} includedeb ${shellQuote(codename)} ${shellQuote(remoteDeb)}"`,
  );
  log('APT: repository updated');
}

function publishYum(rpmPath) {
  const ssh = process.env.PACKAGE_REPO_SSH;
  const yumRoot = process.env.PACKAGE_REPO_YUM_ROOT;
  if (!ssh || !yumRoot) {
    warn('YUM: skipped (set PACKAGE_REPO_SSH and PACKAGE_REPO_YUM_ROOT to enable)');
    return;
  }
  if (!fs.existsSync(rpmPath)) {
    warn(`YUM: skipped (.rpm not found: ${rpmPath})`);
    return;
  }

  const remoteRpm = `${yumRoot}/${path.basename(rpmPath)}`;
  log(`YUM: uploading ${path.basename(rpmPath)} → ${ssh}:${remoteRpm}`);
  run(`scp "${rpmPath}" "${ssh}:${remoteRpm}"`);
  run(`ssh "${ssh}" "createrepo_c --update ${shellQuote(yumRoot)}"`);
  log('YUM: repository updated');
}

function shellQuote(value) {
  return `'${String(value).replace(/'/g, `'\\''`)}'`;
}

function buildHomebrewCask({ pkgArm64, pkgX64 }) {
  if (!fs.existsSync(pkgArm64) || !fs.existsSync(pkgX64)) {
    warn('Homebrew: skipped (mac .pkg artifacts missing from build/)');
    return null;
  }

  const shaArm64 = sha256File(pkgArm64);
  const shaX64 = sha256File(pkgX64);
  const urlArm64 = `${GITHUB_RELEASE_BASE}/${version}/MyHomeGames-${version}-mac-arm64.pkg`;
  const urlX64 = `${GITHUB_RELEASE_BASE}/${version}/MyHomeGames-${version}-mac-x64.pkg`;

  return `# Generated by scripts/publish-package-repos.js — do not edit by hand.
cask "myhomegames-server" do
  version "${version}"

  if Hardware::CPU.arm?
    sha256 "${shaArm64}"
    url "${urlArm64}"
  else
    sha256 "${shaX64}"
    url "${urlX64}"
  end

  name "MyHomeGames Server"
  desc "Self-hosted game library server"
  homepage "https://myhomegames.vige.it"

  pkg "MyHomeGames-#{version}-mac-#{Hardware::CPU.arm? ? 'arm64' : 'x64'}.pkg"

  uninstall pkgutil: "com.myhomegames.server"
end
`;
}

function publishHomebrewTap(caskBody) {
  if (!caskBody) return;

  const tapRepo = process.env.HOMEBREW_TAP_REPO;
  if (!tapRepo) {
    warn('Homebrew tap: skipped (set HOMEBREW_TAP_REPO to enable)');
    return;
  }

  const tmp = fs.mkdtempSync(path.join(os.tmpdir(), 'mhg-homebrew-tap-'));
  try {
    run(`git clone --depth 1 ${shellQuote(tapRepo)} ${shellQuote(tmp)}`, { cwd: os.tmpdir() });
    const tapCaskDir = path.join(tmp, 'Casks');
    fs.mkdirSync(tapCaskDir, { recursive: true });
    fs.writeFileSync(path.join(tapCaskDir, 'myhomegames-server.rb'), caskBody, 'utf8');

    run('git add Casks/myhomegames-server.rb', { cwd: tmp });
    const status = execSync('git status --porcelain', { cwd: tmp, encoding: 'utf8' });
    if (!status.trim()) {
      log('Homebrew tap: no changes to push');
      return;
    }
    run(`git commit -m ${shellQuote(`chore(cask): myhomegames-server ${version}`)}`, {
      cwd: tmp,
    });
    run('git push origin HEAD', { cwd: tmp });
    log('Homebrew tap: pushed');
  } catch (err) {
    warn(`Homebrew tap: push failed (${err.message})`);
  } finally {
    fs.rmSync(tmp, { recursive: true, force: true });
  }
}

function main() {
  log(`Publishing artifacts for version ${version}`);

  const { deb, rpm, pkgArm64, pkgX64 } = artifactPaths();

  try {
    publishApt(deb);
  } catch (err) {
    warn(`APT: failed (${err.message})`);
  }

  try {
    publishYum(rpm);
  } catch (err) {
    warn(`YUM: failed (${err.message})`);
  }

  try {
    const cask = buildHomebrewCask({ pkgArm64, pkgX64 });
    publishHomebrewTap(cask);
  } catch (err) {
    warn(`Homebrew: failed (${err.message})`);
  }

  log('Done');
}

main();
